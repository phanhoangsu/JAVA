public class thamChieu {
    //Fields
    private int d;
    private int e;
    private int f;

    // Contructor

    public thamChieu(int d, int e, int f) {
        this.d = d;
        this.e = e;
        this.f = f;
    }

    //Getter/Setter

    public int getD() {
        return d;
    }

    public void setD(int d) {
        this.d = d;
    }

    public int getE() {
        return e;
    }

    public void setE(int e) {
        this.e = e;
    }

    public int getF() {
        return f;
    }

    public void setF(int f) {
        this.f = f;
    }
}
